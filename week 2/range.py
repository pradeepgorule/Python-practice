# range(start, stop, step)

# start → optional (default 0)
# stop → required
# step → optional (default 1)

# range(5)   # 0 1 2 3 4

for i in range(5):
    print(f" i {i}")

# range(1,5) # 1 2 3 4 

for j in range(1,5):
    print(f" j {j}")

# range(1,5,2) # 1 3 5 7 9

for k in range(1,10,2):
    print(f" k {k}")

